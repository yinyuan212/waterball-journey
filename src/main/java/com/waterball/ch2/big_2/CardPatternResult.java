package com.waterball.ch2.big_2;

import java.util.List;

public class CardPatternResult {

    private String result;

    private List<Card> topPlay;

    private String topPlayName;

    public String getResult() {
        return result;
    }

    public void setResult(String result) {
        this.result = result;
    }

    public List<Card> getTopPlay() {
        return topPlay;
    }

    public void setTopPlay(List<Card> topPlay) {
        this.topPlay = topPlay;
    }

    public String getTopPlayName() {
        return topPlayName;
    }

    public void setTopPlayName(String topPlayName) {
        this.topPlayName = topPlayName;
    }
}
